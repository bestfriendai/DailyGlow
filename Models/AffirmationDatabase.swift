    // MARK: - Get Random Affirmation
    static func getRandomAffirmation() -> Affirmation {
        let allAffirmations = getAllAffirmations()
        return allAffirmations.randomElement() ?? Affirmation(text: "I am grateful for this moment", category: .gratitude)
    }
    
    // MARK: - Get Time-Based Affirmation
    static func getTimeBasedAffirmation() -> Affirmation {
        let hour = Calendar.current.component(.hour, from: Date())
        
        switch hour {
        case 5..<9:
            // Early morning - strength/motivation
            return motivation.randomElement().map { Affirmation(text: $0, category: .strength) } ?? getRandomAffirmation()
        case 9..<12:
            // Morning - success/confidence
            let combined = success + confidence
            return combined.randomElement().map { Affirmation(text: $0, category: .success) } ?? getRandomAffirmation()
        case 12..<14:
            // Midday - gratitude
            return gratitude.randomElement().map { Affirmation(text: $0, category: .gratitude) } ?? getRandomAffirmation()
        case 14..<17:
            // Afternoon - strength/abundance
            let combined = motivation + abundance
            return combined.randomElement().map { Affirmation(text: $0, category: .strength) } ?? getRandomAffirmation()
        case 17..<20:
            // Evening - gratitude/relationships
            let combined = gratitude + love
            return combined.randomElement().map { Affirmation(text: $0, category: .gratitude) } ?? getRandomAffirmation()
        case 20..<23:
            // Night - peace/self-love
            let combined = peace + selfLove
            return combined.randomElement().map { Affirmation(text: $0, category: .peace) } ?? getRandomAffirmation()
        default:
            // Late night - peace
            return peace.randomElement().map { Affirmation(text: $0, category: .peace) } ?? getRandomAffirmation()
        }
    }
    
